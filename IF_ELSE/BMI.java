package IF_ELSE;

import java.util.Scanner;

public class BMI {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    double bmi = sc.nextDouble();

    if (bmi < 18.5)
      System.out.println("Underweight");
    else if (bmi < 25)
      System.out.println("Normal");
    else
      System.out.println("Overweight");
  }
}

