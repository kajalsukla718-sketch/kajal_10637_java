package IF_ELSE;

import java.util.Scanner;

public class range {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int n = sc.nextInt();
    if (n >= 1 && n <= 100)
      System.out.println("Within Range");
    else
      System.out.println("Out of Range");
  }
}

