public class profitLoss {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int cost = sc.nextInt();
    int sell = sc.nextInt();
    if (sell > cost) {
      System.out.println("Profit");
    } else if (sell < cost) {
      System.out.println("loss");
    } else {
      System.out.println("No profit or lsos");
    }
  }
}

