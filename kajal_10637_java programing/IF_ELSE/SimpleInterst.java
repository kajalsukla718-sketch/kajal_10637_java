package IF_ELSE;

import java.util.Scanner;

public class SimpleInterst {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int p = sc.nextInt();
    int t = sc.nextInt();

    int r = sc.nextInt();

    int si = (p * t * r) / 100;

    if (si > 100)
      System.out.println("High Interest");
    else
      System.out.println("Low Interest");
  }
}

